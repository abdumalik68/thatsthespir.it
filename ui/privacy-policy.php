<div class="content">
<strong>Like millions of other website owners, I use Google Analytics on That's The Spirit!.</strong>

Google Analytics is a piece of software that grabs data about my visitors (you). It’s something like an advanced server log.
<h2>What does Google Analytics record?</h2>
<ul>
	<li>What website you came from to get here.</li>
	<li>How long you stay for.</li>
	<li>What kind of computer you’re using.</li>
	<li>And quite a bit more.</li>
</ul>
<h2>What do I do with your data?</h2>
The tracking information allows me to better understand the kind of people who come to my site and what content they’re reading. This allows me to make better decisions about design and writing.

Occasionally, I will compile aggregate statistics about the number of visitors this site receives and browsers being used. No personally identifying data is included in this type of reporting.

All of my activity falls within the bounds of the <a href="http://www.google.com/analytics/tos.html">Google Analytics Terms of Service</a>.

<strong>Want to opt out of tracking?</strong>

You can <a href="http://www.google.com/privacy_ads.html">opt out of Google’s advertising tracking cookie</a> or <a href="http://tools.google.com/dlpage/gaoptout?hl=en">use a browser plugin to opt out of all Google Analytics tracking software.</a>
